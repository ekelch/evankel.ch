    <p>The Norwegian heavy water sabotage (Bokmål: Tungtvannsaksjonen; Nynorsk: Tungtvassaksjonen) was a series of
        Allied-led efforts to halt German heavy water production via hydroelectric plants in Nazi Germany-occupied
        Norway during World War II, involving both Norwegian commandos and Allied bombing raids. During the war, the
        Allies sought to inhibit the German development of nuclear weapons with the removal of heavy water and the
        destruction of heavy-water production plants. The Norwegian heavy water sabotage was aimed at the 60 MW Vemork
        power station at the Rjukan waterfall in Telemark. </p>
    <p><br>
        The hydroelectric power plant at Vemork was built in 1934. It was the world's first site to mass-produce heavy
        water (as a byproduct of nitrogen fixing), with a capacity of 12 tonnes per year. Before the German invasion of
        Norway on 9 April 1940, the French Deuxième Bureau removed 185 kilograms (408 lb) of heavy water from the
        Vemork plant in then-neutral Norway.
    </p>
